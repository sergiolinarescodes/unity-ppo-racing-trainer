        private static void RunBatch(int n)
        {
            // Random base seed each click + no wipe: each menu invocation
            // EXTENDS the library with fresh circuits. Existing files are
            // preserved (Generate skips File.Exists) and signature-dedup
            // catches accidental seed collisions across runs.
            // Seed range capped well below int.MaxValue so baseSeed+n never
            // overflows. Wide enough that collision with prior seeds is
            // astronomically unlikely.
            int baseSeed = new System.Random().Next(1, int.MaxValue - 1_000_000);
            int generated = Generate(n, baseSeed);
            Debug.Log($"[AuthoredClosureLib] menu run complete: {generated}/{n} circuits saved (baseSeed={baseSeed}).");
        }

        /// <summary>
        /// Generates <paramref name="batchSize"/> circuits with seeds
        /// <c>baseSeed..baseSeed+batchSize-1</c>. Returns the number of
        /// circuits successfully written. Skips already-existing files (no
        /// overwrite) so re-running fills gaps.
        /// </summary>
        public static int Generate(int batchSize, int baseSeed)
        {
            string outDir = Path.Combine("circuits", OutputFolderName);
            Directory.CreateDirectory(outDir);

            int authoredAvailable = AuthoredTrackJsonStore.ListIds().Count;
            if (authoredAvailable == 0)
            {
                Debug.LogError("[AuthoredClosureLib] no authored tracks on disk at " +
                               $"{Application.persistentDataPath}/AuthoredTracks. Save at least one " +
                               "track via the Track Editor scenario (F2) before running this menu.");
                return 0;
            }

            var stage = CurriculumStages.Default;

            int written = 0;
            int skipped = 0;
            int failed = 0;
            int rejected = 0;
            int duplicates = 0;
            bool cancelled = false;
            // Dedup signature → seed where it was first seen. Two seeds that
            // produce the same normalised placement list are treated as the
            // same circuit; later duplicates are rejected so the library
            // contains 100 distinct loops, not 100 copies of the easy oval.
            var signatureToSeed = LoadExistingSignatures(outDir);
            try
            {
                for (int i = 0; i < batchSize; i++)
                {
                    int seed = baseSeed + i;

                    // Cancelable progress bar — user clicks Cancel to bail
                    // mid-batch. Refresh frequency: every iteration. Returning
                    // true from DisplayCancelableProgressBar means the user hit
                    // Cancel; we break and report partial results.
                    string label = $"seed {seed} ({i + 1}/{batchSize}) — written={written} failed={failed} rejected={rejected}";
                    if (UnityEditor.EditorUtility.DisplayCancelableProgressBar(
                            "Authored Closure Circuit Library", label, (float)i / batchSize))
                    {
                        cancelled = true;
                        break;
                    }

                    string outPath = Path.Combine(outDir, $"circuit_{seed:0000}.json");
                    if (File.Exists(outPath))
                    {
                        skipped++;
                        continue;
                    }

                    var ctx = BuildContext();
                    try
                    {
                        var result = TryGenerateOne(ctx, seed, stage);
                        if (result.Success && ctx.Loop.IsLoopClosed
                            && ctx.Loop.TryGetCurrentLoop(out var loop))
                        {
                            // Quality gate: reject degenerate loops (oval
                            // bias, too short, too few pieces, low turn
                            // density). Then dedup: identical placement
                            // signatures across seeds collapse into one
                            // entry so the library shows 100 *distinct*
                            // loops, not 100 copies of the same oval.
                            if (!IsAcceptableCircuit(ctx, loop, result, out var rejectReason))
                            {
                                rejected++;
                                Debug.Log($"[AuthoredClosureLib] seed={seed} rejected: {rejectReason} {RenderAscii(ctx)}");
                                LogAsciiGrid(ctx, seed);
                            }
                            else
                            {
                                string signature = ComputeCircuitSignature(ctx);
                                if (signatureToSeed.TryGetValue(signature, out int firstSeen))
                                {
                                    duplicates++;
                                    Debug.Log($"[AuthoredClosureLib] seed={seed} duplicate of seed={firstSeen} (signature collision)");
                                }
                                else
                                {
                                    WriteCircuitFile(outPath, ctx, loop, seed, stage, result);
                                    signatureToSeed[signature] = seed;
                                    written++;
                                    Debug.Log($"[AuthoredClosureLib] seed={seed} ACCEPTED (pieces={result.PlacedPieces} length={loop.TotalLength:F1}m) {RenderAscii(ctx)}");
                                    LogAsciiGrid(ctx, seed);
                                }
                            }
                        }
                        else
                        {
                            failed++;
                            Debug.LogWarning($"[AuthoredClosureLib] seed={seed} failed: {result.FailureReason} {RenderAscii(ctx)}");
                            LogAsciiGrid(ctx, seed);
                        }
                    }
                    catch (Exception e)
                    {
                        failed++;
                        Debug.LogWarning($"[AuthoredClosureLib] seed={seed} threw {e.GetType().Name}: {e.Message}");
                    }
                    finally
                    {
                        ctx.Dispose();
                    }
                }
            }
            finally
            {
                UnityEditor.EditorUtility.ClearProgressBar();
            }

            string status = cancelled ? "CANCELLED " : "";
            Debug.Log($"[AuthoredClosureLib] {status}outDir='{outDir}' batch={batchSize} " +
                      $"written={written} skipped(existing)={skipped} duplicates={duplicates} " +
                      $"rejected={rejected} failed={failed} authoredCards={authoredAvailable}.");
            UnityEditor.AssetDatabase.Refresh();
            return written;
        }

        /// <summary>
        /// Quality gate. Rejects: too-short loops, too-few-pieces micro-ovals,
        /// flat aspect-ratio "straight oval" layouts (the user's complaint —
        /// long horizontal corridor + short return), and loops with too few
        /// turns to count as a real circuit. Tune the constants here if the
        /// rejection rate is too aggressive.
        /// </summary>
        /// <summary>
        /// Render an ASCII map of every cell currently in the placement
        /// occupancy. Used in batch-log output so failed/rejected/accepted
        /// circuits are visually inspectable without re-running. Capped at
        /// 60×60 cells; larger grids get truncated with a marker.
        /// </summary>
        private static string RenderAscii(GenContext ctx)
        {
            // Returns a single-line summary for the headline log; the row-by-row
            // grid is emitted separately via LogAsciiGrid so Unity Console
            // doesn't collapse the multi-line message into the first line only.
            var occ = ctx.Placement.Occupancy;
            if (occ == null || occ.Count == 0) return "(empty)";
            int minX = int.MaxValue, minY = int.MaxValue;
            int maxX = int.MinValue, maxY = int.MinValue;
            foreach (var kv in occ)
            {
                if (kv.Key.X < minX) minX = kv.Key.X;
                if (kv.Key.Y < minY) minY = kv.Key.Y;
                if (kv.Key.X > maxX) maxX = kv.Key.X;
                if (kv.Key.Y > maxY) maxY = kv.Key.Y;
            }
            int w = maxX - minX + 1;
            int h = maxY - minY + 1;
            // Distinct shape kinds — user wants 10+; emit so we can see at a glance.
            var distinct = new HashSet<string>();
            foreach (var p in ctx.Placement.Placed)
                distinct.Add(p.Shape.Id);
            return $"  bbox={w}x{h} cells={occ.Count} distinctKinds={distinct.Count}";
        }

        private static void LogAsciiGrid(GenContext ctx, int seed)
        {
            var occ = ctx.Placement.Occupancy;
            if (occ == null || occ.Count == 0) return;
            int minX = int.MaxValue, minY = int.MaxValue;
            int maxX = int.MinValue, maxY = int.MinValue;
            foreach (var kv in occ)
            {
                if (kv.Key.X < minX) minX = kv.Key.X;
                if (kv.Key.Y < minY) minY = kv.Key.Y;
                if (kv.Key.X > maxX) maxX = kv.Key.X;
                if (kv.Key.Y > maxY) maxY = kv.Key.Y;
            }
            int w = maxX - minX + 1;
            int h = maxY - minY + 1;
            if (w > 110 || h > 110) { Debug.Log($"[AsciiGrid s{seed}] (too large: {w}x{h})"); return; }
            for (int y = maxY; y >= minY; y--)
            {
                var sb = new System.Text.StringBuilder(w);
                for (int x = minX; x <= maxX; x++)
                    sb.Append(occ.ContainsKey(new GridPosition(x, y)) ? '#' : '.');
                Debug.Log($"[AsciiGrid s{seed}] {sb}");
            }
        }

        private const float MinAcceptableLengthMeters = 75f;
        private const int MinAcceptablePieces = 30;
        private const float MaxAcceptableAspectRatio = 4.5f;
        private const int MinAcceptableTurnPieces = 6;

        private static bool IsAcceptableCircuit(GenContext ctx, ClosedLoop loop,
            GenerationResult result, out string reason)
        {
            if (result.PlacedPieces < MinAcceptablePieces)
            {
                reason = $"only {result.PlacedPieces} pieces (min {MinAcceptablePieces})";
                return false;
            }
            if (loop.TotalLength < MinAcceptableLengthMeters)
            {
                reason = $"length {loop.TotalLength:F1}m below floor {MinAcceptableLengthMeters}m";
                return false;
            }

            // Aspect-ratio gate — if max(W,H) / min(W,H) > MaxAcceptable, this
            // is a "long flat oval" (the user's complaint). Compute on the
            // placement bounding box — it's tight and cheap.
            int minX = int.MaxValue, minY = int.MaxValue;
            int maxX = int.MinValue, maxY = int.MinValue;
            int curveCount = 0;
            foreach (var p in ctx.Placement.Placed)
            {
                if (p.Origin.X < minX) minX = p.Origin.X;
                if (p.Origin.Y < minY) minY = p.Origin.Y;
                if (p.Origin.X > maxX) maxX = p.Origin.X;
                if (p.Origin.Y > maxY) maxY = p.Origin.Y;
                if (ctx.PieceCatalog.TryGet(p.Shape, out var def))
                {
                    if (def.Family == TrackPieceFamily.Curve
                        || def.Family == TrackPieceFamily.DiagonalCurve)
                        curveCount++;
                }
            }
            float w = Mathf.Max(1, maxX - minX + 1);
            float h = Mathf.Max(1, maxY - minY + 1);
            float aspect = Mathf.Max(w, h) / Mathf.Min(w, h);
            if (aspect > MaxAcceptableAspectRatio)
            {
                reason = $"aspect ratio {aspect:F2} > {MaxAcceptableAspectRatio} (flat oval)";
                return false;
            }
            if (curveCount < MinAcceptableTurnPieces)
            {
                reason = $"only {curveCount} curve pieces (min {MinAcceptableTurnPieces} — straight oval)";
                return false;
            }

            reason = null;
            return true;
        }

        /// <summary>
        /// Translation-invariant signature of a closed-loop layout. Two seeds
        /// that placed the same shape ids at the same relative offsets/facings
        /// hash to the same signature → second one is a duplicate. The shift
        /// to (0,0) makes the dedup invariant under origin choice, so the
        /// generator can change its anchor cell across seeds without breaking
        /// dedup.
        /// </summary>
        private static string ComputeCircuitSignature(GenContext ctx)
        {
            int minX = int.MaxValue, minY = int.MaxValue;
            foreach (var p in ctx.Placement.Placed)
            {
                if (p.Origin.X < minX) minX = p.Origin.X;
                if (p.Origin.Y < minY) minY = p.Origin.Y;
            }
            var entries = new List<string>(ctx.Placement.Placed.Count);
            foreach (var p in ctx.Placement.Placed)
            {
                entries.Add($"{p.Shape.Id}:{p.Origin.X - minX}:{p.Origin.Y - minY}:{(int)p.Facing}");
            }
            entries.Sort(StringComparer.Ordinal);
            return string.Join("|", entries);
        }

        /// <summary>
        /// On startup, hash every existing JSON in the output folder so a
        /// re-run that already wrote 50 circuits doesn't re-generate ones
        /// that match (signature-wise) what's already on disk.
        /// </summary>
        private static Dictionary<string, int> LoadExistingSignatures(string outDir)
        {
            var dict = new Dictionary<string, int>();
            if (!Directory.Exists(outDir)) return dict;
            foreach (var f in Directory.GetFiles(outDir, "*.json"))
            {
                try
                {
                    var existing = JsonUtility.FromJson<CircuitFile>(File.ReadAllText(f));
                    if (existing?.Placements == null || existing.Placements.Count == 0) continue;
                    int minX = int.MaxValue, minY = int.MaxValue;
                    foreach (var p in existing.Placements)
                    {
                        if (p.X < minX) minX = p.X;
                        if (p.Y < minY) minY = p.Y;
                    }
                    var entries = new List<string>(existing.Placements.Count);
                    foreach (var p in existing.Placements)
                    {
                        entries.Add($"{p.ShapeId}:{p.X - minX}:{p.Y - minY}:{p.Facing}");
                    }
                    entries.Sort(StringComparer.Ordinal);
                    string sig = string.Join("|", entries);
                    if (!dict.ContainsKey(sig)) dict[sig] = existing.Seed;
                }
                catch
                {
                    // Skip unreadable / mis-shaped files; they'll be left alone.
                }
            }
            return dict;
        }

        private static GenContext BuildContext()
        {
            var ctx = new GenContext();
            ctx.EventBus = new ScenarioEventBus();
            ctx.Factory = new ScenarioGameObjectFactory();

            ctx.Terrain = new TerrainService(ctx.EventBus);
            ctx.Terrain.Initialize(new TerrainBuildOptions(TerrainWidth, TerrainDepth, 0, TrackPieceConstants.CellSize));

            ctx.PieceCatalog = new TrackPieceCatalog();
            TrackPieceCatalogSeeder.Seed(ctx.PieceCatalog);

            var meshBuilder = new TrackPieceMeshBuilder(new FlatHeightAdapter());
            var validators = new List<ITrackPlacementValidator>
            {
                new BoundsValidator(),
                new OverlapValidator(),
                new TerrainCompatibilityValidator()
            };
            ctx.Collision = new TrackCollisionService();
            ctx.Placement = new TrackPlacementService(ctx.EventBus, ctx.PieceCatalog, meshBuilder,
                validators, ctx.Terrain, ctx.Factory, TrackPalette.Default, ctx.Collision);

            ctx.ShapeCatalog = new TrackShapeCatalog();
            ctx.AuthoredCount = AuthoredCardLoader.LoadAllInto(ctx.ShapeCatalog, ctx.PieceCatalog);
            ctx.ClosureCount = ClosureTransitionShapes.Register(ctx.ShapeCatalog, ctx.PieceCatalog,
                longStraightCells: DefaultAnchorStraightCells);

            var preview = new ShapePreviewService(ctx.PieceCatalog, validators, ctx.Terrain, ctx.Placement);
            ctx.ShapePlacement = new ShapePlacementService(ctx.EventBus, preview, ctx.Placement);

            ctx.Loop = new ClosedLoopService(ctx.EventBus, ctx.Placement, ctx.PieceCatalog, ctx.Terrain);

            ctx.Native = new RealisticNativeCatalog(ctx.ShapeCatalog, ctx.PieceCatalog);
            ctx.Generator = new RealisticTrackGenerator(
                ctx.EventBus, ctx.Placement, ctx.ShapePlacement, ctx.Loop,
                ctx.ShapeCatalog, ctx.PieceCatalog, ctx.Terrain, ctx.Native);

            return ctx;
        }

        private static GenerationResult TryGenerateOne(GenContext ctx, int seed, CurriculumStage stage)
        {
            // Per-seed random cardinal facing — without this, every loop launches
            // east → all layouts elongate east-west → flat-oval bias even after
            // scoring tweaks. Hash the seed into one of {N, E, S, W} (cardinal-only
            // because the placement pipeline rejects diagonal facings).
            uint h = unchecked((uint)seed * 2654435761u);
            h ^= h >> 13; h *= 1274126177u; h ^= h >> 16;
            var facings = new[] { TrackDirection.East, TrackDirection.North, TrackDirection.West, TrackDirection.South };
            var initialFacing = facings[(int)(h % 4)];

            var cfg = new RealisticTrackGenerationConfig(
                Seed: seed,
                Origin: new GridPosition(OriginX, OriginZ),
                InitialFacing: initialFacing,
                Stage: stage,
                RecipeAttempts: DefaultAttempts,
                TargetLengthCells: DefaultTargetLengthCells,
                TurnDensity: DefaultTurnDensity,
                BeamWidth: DefaultBeamWidth,
                PerAttemptTimeoutMs: 3000,
                MaxSearchSteps: 80,
                MinMainStraightCells: 4);  // 4-cell legs → more turns, less rectangular bias
            return ctx.Generator.Generate(cfg);
        }

        private static void WriteCircuitFile(string path, GenContext ctx, ClosedLoop loop,
            int seed, CurriculumStage stage, GenerationResult result)
        {
            var placements = new List<CircuitFile.Placement>(ctx.Placement.Placed.Count);
            foreach (var p in ctx.Placement.Placed)
            {
                placements.Add(new CircuitFile.Placement
                {
                    ShapeId = p.Shape.Id,
                    X = p.Origin.X,
                    Y = p.Origin.Y,
                    Facing = (int)p.Facing,
                });
            }

            // Anchors MUST come from loop.Anchors (post-CCW-flip in
            // ClosedLoopService.BuildClosedLoop) — that is the canonical
            // sequence the indices LapStartAnchorIndex / MicroBoundaryAnchor
            // refer to. Serializing a separate TrackChainExtractor.Extract()
            // run here would drop the CCW flip and make the indices reference
            // a different order than the dashboard's polyline → start label
            // lands on the wrong anchor.
            var anchors = SerializeLoopAnchors(loop);

            // Per-piece overlay segments for closure-only shapes — the Python
            // viewer renders these in a contrasting colour so the user can
            // see exactly which arcs were generator-bridged vs authored.
            var closureSegments = ExtractClosureSegments(ctx);

            var microBoundary = loop.Sectors.MicroBoundaryAnchor;
            int[] microBoundaryArr;
            if (microBoundary != null && microBoundary.Count > 0)
            {
                microBoundaryArr = new int[microBoundary.Count];
                for (int i = 0; i < microBoundary.Count; i++) microBoundaryArr[i] = microBoundary[i];
            }
            else
            {
                microBoundaryArr = System.Array.Empty<int>();
            }

            var rec = new CircuitFile
            {
                Id = $"authored_closure_{seed:0000}",
                TotalLength = loop.TotalLength,
                AnchorCount = anchors.Count,
                AuthoredCardCount = ctx.AuthoredCount,
                ClosureCardCount = ctx.ClosureCount,
                Seed = seed,
                Placements = placements,
                Anchors = anchors,
                ClosureSegments = closureSegments,
                // Single source of truth: the indices the simulator's lap
                // counter actually uses. The Python dashboard reads these as
                // overrides (server.py _compute_sector_meta) so the START
                // label snaps to the same anchor as the car's spawn pose.
                LapStartAnchorIndex = loop.LapStartAnchorIndex,
                MicroBoundaryAnchor = microBoundaryArr,
            };
            File.WriteAllText(path, JsonUtility.ToJson(rec, prettyPrint: true));
        }

        // Serialize ClosedLoop.Anchors (already CCW-canonical) so on-disk
        // anchor order matches the index domain of LapStartAnchorIndex /
        // MicroBoundaryAnchor. The blanket [Serializable] CircuitFile.Anchor
        // shape stays the same (X, Z).
        private static List<CircuitFile.Anchor> SerializeLoopAnchors(ClosedLoop loop)
        {
            var output = new List<CircuitFile.Anchor>();
            if (loop.Anchors == null) return output;
            for (int i = 0; i < loop.Anchors.Count; i++)
            {
                var a = loop.Anchors[i];
                output.Add(new CircuitFile.Anchor
                {
                    X = a.WorldPos.x,
                    Z = a.WorldPos.z,
                });
            }
            return output;
        }

        private static List<CircuitFile.Segment> ExtractClosureSegments(GenContext ctx)
        {
            var segments = new List<CircuitFile.Segment>();
            var closureIdsRaw = ctx.Generator?.LastClosurePieceIds;
            if (closureIdsRaw == null || closureIdsRaw.Count == 0) return segments;
            var closureIds = new HashSet<TrackPieceId>(closureIdsRaw);
            try
            {
                var extractor = new TrackChainExtractor(ctx.PieceCatalog);
                foreach (var p in ctx.Placement.Placed)
                {
                    if (!closureIds.Contains(p.Id)) continue;
                    if (!ctx.PieceCatalog.TryGet(p.Shape, out var def)) continue;
                    var spine = extractor.BuildWorldSpine(def, p.Origin, p.Facing,
                        TrackPieceConstants.CellSize);
                    if (spine == null || spine.Length < 2) continue;
                    var seg = new CircuitFile.Segment
                    {
                        Anchors = new List<CircuitFile.Anchor>(spine.Length),
                    };
                    for (int i = 0; i < spine.Length; i++)
                    {
                        seg.Anchors.Add(new CircuitFile.Anchor
                        {
                            X = spine[i].WorldPos.x,
                            Z = spine[i].WorldPos.z,
                        });
                    }
                    segments.Add(seg);
                }
            }
            catch
            {
                // Decorative; if extraction fails the Python viewer still has
                // the body Anchors polyline to draw.
            }
            return segments;
        }

        private static List<CircuitFile.Anchor> ExtractAnchors(GenContext ctx)
        {
            var output = new List<CircuitFile.Anchor>();
            try
            {
                var extractor = new TrackChainExtractor(ctx.PieceCatalog);
                var chains = extractor.Extract(ctx.Placement.Placed, TrackPieceConstants.CellSize);
                if (chains == null) return output;
                // Pick the longest chain — closed loops produce one chain;
                // pathological multi-chain layouts are already filtered by
                // the generator's chainCount guard.
                int bestChain = -1, bestLen = -1;
                for (int c = 0; c < chains.Count; c++)
                {
                    var chain = chains[c];
                    if (chain == null) continue;
                    if (chain.Length > bestLen) { bestLen = chain.Length; bestChain = c; }
                }
                if (bestChain < 0) return output;
                var chosen = chains[bestChain];
                for (int i = 0; i < chosen.Length; i++)
                {
                    output.Add(new CircuitFile.Anchor
                    {
                        X = chosen[i].WorldPos.x,
                        Z = chosen[i].WorldPos.z,
                    });
                }
            }
            catch
            {
                // Anchor export is decorative for the viewer; if extraction
                // throws, fall back to placements-only — Python renders cells.
            }
            return output;
        }

        // Schema mirrors the private CircuitFile inside CurriculumGeneratorSelector
        // — JsonUtility ignores unknown fields on read, so the additive fields
        // (Seed / AuthoredCardCount / ClosureCardCount) don't break the curriculum
        // replay path if anyone wires this folder into LibraryDirFor in the future.
        [Serializable]
        public sealed class CircuitFile
        {
            public string Id;
            public float TotalLength;
            public int AnchorCount;
            public int AuthoredCardCount;
            public int ClosureCardCount;
            public int Seed;
            public List<Placement> Placements;
            // Ribbon-centerline samples in world XZ. Optional — older files
            // written before this field existed deserialize with Anchors=null
            // and the Python viewer falls back to cell-square rendering.
            // ORDER MATTERS: indexed by LapStartAnchorIndex / MicroBoundaryAnchor.
            public List<Anchor> Anchors;
            // Per-closure-piece spine overlays. Each segment is the
            // world-space spine of one piece committed by a "closure:*"
            // shape. The Python viewer draws these in a contrasting colour
            // over the body Anchors polyline.
            public List<Segment> ClosureSegments;
            // Index into Anchors for the simulator's canonical start/finish
            // line. Single source of truth for the dashboard's START label —
            // without this, server.py falls back to a Python-side longest-
            // straight heuristic whose curvature threshold disagrees with C#
            // on rectangular layouts (two long similar straights → wrong
            // pick). Legacy files without this field deserialize to 0;
            // server.py treats 0 as "no override" via _compute_sector_meta's
            // bounds check.
            public int LapStartAnchorIndex;
            // K=9 anchor indices marking micro-sector boundaries, in lap
            // traversal order with sector 0 at LapStartAnchorIndex. Empty/null
            // for legacy files; server.py falls back to evenly-spaced sectors.
            public int[] MicroBoundaryAnchor;

            [Serializable]
            public sealed class Placement
            {
                public string ShapeId;
                public int X;
                public int Y;
                public int Facing;
            }

            [Serializable]
            public sealed class Anchor
            {
                public float X;
                public float Z;
            }

            [Serializable]
            public sealed class Segment
            {
                public List<Anchor> Anchors;
            }
        }

