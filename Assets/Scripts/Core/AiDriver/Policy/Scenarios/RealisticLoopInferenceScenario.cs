        private static bool AnchorsEqual(List<CircuitFile.Anchor> a, List<CircuitFile.Anchor> b)
        {
            if (a == null && b == null) return true;
            if (a == null || b == null) return false;
            if (a.Count != b.Count) return false;
            for (int i = 0; i < a.Count; i++)
            {
                if (Mathf.Abs(a[i].X - b[i].X) > 1e-4f) return false;
                if (Mathf.Abs(a[i].Z - b[i].Z) > 1e-4f) return false;
            }
            return true;
        }

        private static bool IntArraysEqual(int[] a, int[] b)
        {
            int an = a?.Length ?? 0;
            int bn = b?.Length ?? 0;
            if (an != bn) return false;
            for (int i = 0; i < an; i++) if (a[i] != b[i]) return false;
            return true;
        }

        // Returns the raw JSON value of "<fieldName>": from the input text, or null
        // if absent. Walks bracket depth so nested arrays/objects (Walls = float[][])
        // are captured wholesale. Mirrors the strip-and-inject style used by
        // CircuitBarrierExportScenario so the two writers stay compatible.
        private static string ExtractFieldJson(string json, string fieldName)
        {
            string key = "\"" + fieldName + "\":";
            int idx = json.IndexOf(key, StringComparison.Ordinal);
            if (idx < 0) return null;
            int p = idx + key.Length;
            while (p < json.Length && char.IsWhiteSpace(json[p])) p++;
            if (p >= json.Length) return null;
            int end;
            char open = json[p];
            if (open == '[' || open == '{')
            {
                char close = open == '[' ? ']' : '}';
                int depth = 0;
                for (end = p; end < json.Length; end++)
                {
                    char c = json[end];
                    if (c == '"') { end = SkipStringLiteral(json, end); continue; }
                    if (c == open) depth++;
                    else if (c == close) { depth--; if (depth == 0) { end++; break; } }
                }
            }
            else if (open == '"')
            {
                end = SkipStringLiteral(json, p) + 1;
            }
            else
            {
                end = p;
                while (end < json.Length && ",}]".IndexOf(json[end]) < 0) end++;
            }
            return json.Substring(p, end - p);
        }

        private static int SkipStringLiteral(string json, int p)
        {
            for (int i = p + 1; i < json.Length; i++)
            {
                if (json[i] == '\\') { i++; continue; }
                if (json[i] == '"') return i;
            }
            return json.Length - 1;
        }

        // Splices "<fragment>" in just before the JSON's closing '}'. Adds the
        // separating comma only when the surrounding object isn't empty.
        private static string InjectBeforeClose(string json, string fragment)
        {
            int close = json.LastIndexOf('}');
            if (close < 0) return json;
            string head = json.Substring(0, close).TrimEnd();
            string tail = json.Substring(close);
            string sep = head.EndsWith("{", StringComparison.Ordinal) ? "" : ",";
            return head + sep + fragment + tail;
        }

