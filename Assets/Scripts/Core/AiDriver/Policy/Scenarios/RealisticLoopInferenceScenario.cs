            "AI Driver — Realistic Loop + Inference (Live)",
            "Generates a realistic F1-style loop and instantiates the trained " +
            "AiDriverAgent_Test prefab (InferenceOnly + ONNX) at the lap start. " +
            "Use this to watch the policy drive on procedurally generated tracks " +
            "without a Python trainer. Same seed → identical track.",
            new[]
            {
                new ScenarioParameter("seed", "Seed", typeof(int), 1234, 0, 1_000_000),
                new ScenarioParameter("targetLengthCells", "Target Length (cells)", typeof(int), 60, 20, 200),
                new ScenarioParameter("turnDensity", "Turn Density (0..1)", typeof(float), 0.4f, 0f, 1f),
                new ScenarioParameter("beamWidth", "Beam Width", typeof(int), 16, 1, 64),
                new ScenarioParameter("attempts", "Attempts (RecipeAttempts)", typeof(int), 6, 1, 16),
                new ScenarioParameter("cameraFollow", "Camera follows car", typeof(bool), false),
            }))
        { }

        protected override void ExecuteInternal(ScenarioParameterOverrides overrides)
        {
            int seed = ResolveParam<int>(overrides, "seed");
            int targetLengthCells = ResolveParam<int>(overrides, "targetLengthCells");
            float turnDensity = ResolveParam<float>(overrides, "turnDensity");
            int beamWidth = ResolveParam<int>(overrides, "beamWidth");
            int attempts = ResolveParam<int>(overrides, "attempts");
            bool cameraFollow = ResolveParam<bool>(overrides, "cameraFollow");

            var stage = CurriculumStages.Default;

            _eventBus = new ScenarioEventBus();
            _factory = new ScenarioGameObjectFactory();

            BuildTerrain();
            BuildTrackServices();
            BuildShapeServices();
            BuildAiDriverServices();
            GenerateLoop(seed, stage, targetLengthCells, turnDensity, beamWidth, attempts);
            BuildRibbon();
            SpawnAgent();
            HookTickProxy();
            ApplyIsoCamera();
            if (cameraFollow) AttachCarFollowCamera();

            Debug.Log($"[RealLoopInfer] Ready. stage={stage.Name} seed={seed} " +
                      $"loopClosed={_loopService.IsLoopClosed} agentReady={_agentReady} " +
                      $"placed={_lastResult.PlacedPieces} length={_lastResult.TotalLength:F1}m");
        }

        protected override ScenarioVerificationResult VerifyInternal(ScenarioParameterOverrides overrides)
        {
            // Missing test prefab or ONNX are environment preconditions, not
            // regressions. Skip cleanly on a clean clone / CI; the LogWarning
            // path in SpawnAgent already explained how to fix it locally.
            if (!string.IsNullOrEmpty(_skipReason))
            {
                return ScenarioVerificationResult.Skip(_skipReason);
            }

            var checks = new List<ScenarioVerificationResult.CheckResult>
            {
                new("loop closed",
                    _loopService != null && _loopService.IsLoopClosed,
                    "ClosedLoopService did not detect a loop after generation"),
                new("agent registered",
                    _agentReady && _agent != null && _agent.IsRegistered,
                    "AiDriverAgentBehaviour did not register with the policy service"),
                new("track query active",
                    _trackQuery != null && _trackQuery.HasLoop,
                    "TrackQueryService reports no active loop"),
            };
            return new ScenarioVerificationResult(checks);
        }

        protected override void OnCleanup()
        {
            // Detach the car-follow camera so the iso view restores cleanly on
            // the next non-follow run (its OnDisable already restores the pose).
            var cam = Camera.main;
            if (cam != null)
            {
                var follow = cam.GetComponent<CarFollowCamera>();
                if (follow != null) UnityEngine.Object.DestroyImmediate(follow);
            }

            if (_tickProxy != null) _tickProxy.OnFixedUpdate = null;
            _tickProxy = null;

            if (_agent != null && _agent.gameObject != null)
            {
                UnityEngine.Object.DestroyImmediate(_agent.gameObject);
            }
            _agent = null;
            _agentReady = false;
            _skipReason = null;

            _ribbonService?.Dispose();
            _generator?.Dispose();
            _native?.Dispose();
            _carSim?.Dispose();
            _trackQuery?.Dispose();
            _loopService?.Dispose();
            _placement?.Dispose();
            _factory?.Dispose();
            _eventBus?.ClearAllSubscriptions();

            _ribbonService = null; _generator = null; _native = null;
            _carSim = null; _trackQuery = null; _loopService = null;
            _placement = null; _shapePlacement = null; _shapePreview = null;
            _shapeCatalog = null; _pieceCatalog = null;
            _factory = null; _eventBus = null;

            DestroyAny(_terrainMesh); _terrainMesh = null;
            DestroyAny(_terrainMaterial); _terrainMaterial = null;

            _carVisual = null;
            _agentObject = null;
            _terrainObject = null;
        }

        // -------------------- build --------------------

        private void BuildTerrain()
        {
            _terrain = new TerrainService(_eventBus);
            _terrain.Initialize(new TerrainBuildOptions(TerrainWidth, TerrainDepth, 0, TrackPieceConstants.CellSize));

            _terrainMeshBuilder = new TerrainMeshBuilder();
            _terrainMesh = _terrainMeshBuilder.Build(_terrain, TerrainPalette.BoneAndSlate, TerrainColorMode.Palette);
            _terrainMaterial = ResolveOpaqueMaterial("AiDriver_RealLoopInfer_TerrainMat");

            _terrainObject = _factory.CreateEmpty("[RealLoopInfer] Terrain");
            _terrainObject.transform.SetParent(SceneRoot.transform, false);

            var mf = _terrainObject.AddComponent<MeshFilter>();
            mf.sharedMesh = _terrainMesh;
            var mr = _terrainObject.AddComponent<MeshRenderer>();
            mr.sharedMaterial = _terrainMaterial;
        }

        private void BuildTrackServices()
        {
            _pieceCatalog = new TrackPieceCatalog();
            TrackPieceCatalogSeeder.Seed(_pieceCatalog);

            _pieceMeshBuilder = new TrackPieceMeshBuilder(new FlatHeightAdapter());
            _validators = new List<ITrackPlacementValidator>
            {
                new BoundsValidator(),
                new OverlapValidator(),
                new TerrainCompatibilityValidator()
            };

            _collision = new TrackCollisionService();
            _placement = new TrackPlacementService(_eventBus, _pieceCatalog, _pieceMeshBuilder,
                _validators, _terrain, _factory, TrackPalette.Default, _collision);
        }

        private void BuildShapeServices()
        {
            _shapeCatalog = new TrackShapeCatalog();
            TrackShapeCatalogSeeder.Seed(_shapeCatalog, _pieceCatalog);
            _shapePreview = new ShapePreviewService(_pieceCatalog, _validators, _terrain, _placement);
            _shapePlacement = new ShapePlacementService(_eventBus, _shapePreview, _placement);
        }

        private void BuildAiDriverServices()
        {
            _loopService = new ClosedLoopService(_eventBus, _placement, _pieceCatalog, _terrain);
            _trackQuery = new TrackQueryService(_eventBus, _loopService, _collision);
            _carSim = new CarSimulationService(_eventBus, _trackQuery, _collision);
            _profileRegistry = new DriverProfileRegistry();
            var manifests = Versions.Manifest.VersionManifestLoader.LoadAll();
            if (!manifests.TryGetValue("latest", out var latestManifest))
            {
                Debug.LogError("[RealisticLoopInferenceScenario] latest manifest missing under Assets/_Bootstrap/Configs/Versions/; using baked defaults.");
                latestManifest = new Versions.Manifest.VersionManifest();
            }
            var versionProfile = new Versions.Manifest.ManifestBackedVersionProfile(
                latestManifest,
                () => NullRewardShaper.Instance);
            _policyService = new AiDriverPolicyService(_eventBus, _carSim, _trackQuery, _loopService, _profileRegistry, versionProfile, _collision);
        }

        private void GenerateLoop(int seed, CurriculumStage stage, int targetLengthCells,
            float turnDensity, int beamWidth, int attempts)
        {
            _native = new RealisticNativeCatalog(_shapeCatalog, _pieceCatalog);
            _generator = new RealisticTrackGenerator(
                _eventBus, _placement, _shapePlacement, _loopService,
                _shapeCatalog, _pieceCatalog, _terrain, _native);

            var cfg = new RealisticTrackGenerationConfig(
                Seed: seed,
                Origin: new GridPosition(OriginX, OriginZ),
                InitialFacing: TrackDirection.East,
                Stage: stage,
                RecipeAttempts: attempts,
                TargetLengthCells: targetLengthCells,
                TurnDensity: turnDensity,
                BeamWidth: beamWidth);

            _lastResult = _generator.Generate(cfg);

            // Same fallback contract as the curriculum selector — never leave the
            // user with an open loop.
            if (!_lastResult.Success)
            {
                Debug.LogWarning($"[RealLoopInfer] realistic generation failed: stage={stage.Name} " +
                                 $"seed={seed}: {_lastResult.FailureReason} — falling back to ShapeBased");
                _placement.Clear();
                var shapeBased = new ShapeBasedLoopGenerator(_eventBus, _shapePlacement,
                    _shapeCatalog, _placement, _loopService);
                _lastResult = shapeBased.Generate(new GenerationConfig(seed,
                    new GridPosition(OriginX, OriginZ), TrackDirection.East, stage));
            }
        }

        private void BuildRibbon()
        {
            _ribbonService = new TrackRibbonService(_eventBus, _placement, _pieceCatalog,
                _terrain, _factory, TrackPalette.Default);
            _ribbonService.Rebuild();
        }

        private void SpawnAgent()
        {
            if (!_loopService.TryGetCurrentLoop(out var loop))
            {
                Debug.LogError("[RealLoopInfer] no closed loop — cannot spawn agent.");
                return;
            }

            // Single source of truth — see ClosedLoop.GetCanonicalStartPose.
            var pose = loop.GetCanonicalStartPose();
            Vector3 spawnPos = pose.position;
            float heading = loop.GetCanonicalStartHeading();

            // Same start-line + sector visualization the trainer uses.
            SectorBoundaryDebugRenderer.MountOn(SceneRoot, _loopService);

            // Instantiating the test prefab carries its BehaviorParameters
            // (Model assigned, BehaviorType=InferenceOnly) and DecisionRequester.
            // Falls back to AiDriverAgent if the test prefab is missing.
            var prefab = Resources.Load<GameObject>(TestPrefabResourcePath);
            if (prefab == null)
            {
                _skipReason =
                    $"prefab missing at Resources/{TestPrefabResourcePath}.prefab — " +
                    "duplicate AiDriverAgent.prefab and set BehaviorType=InferenceOnly + Model.";
                Debug.LogWarning("[RealLoopInfer] " + _skipReason);
                return;
            }

            // Build the GameObject INACTIVE first so we can assign the ModelAsset
            // before ML-Agents' Agent.OnEnable runs (it throws "Can't use
            // BehaviorType InferenceOnly without a model" on enable if the prefab
            // override didn't carry the reference through Instantiate).
            bool wasActive = prefab.activeSelf;
            if (wasActive) prefab.SetActive(false);
            _agentObject = UnityEngine.Object.Instantiate(prefab, spawnPos, Quaternion.identity);
            if (wasActive) prefab.SetActive(true); // restore source asset state
            _agentObject.SetActive(false);
            _agentObject.name = "[RealLoopInfer] Agent";
            _agentObject.transform.SetParent(SceneRoot.transform, false);
            _agentObject.transform.position = spawnPos;

            // Force-assign the ONNX so Agent.OnEnable doesn't throw if the prefab
            // override's m_Model fileID drifted (Sentis vs Barracuda main-asset
            // fileIDs differ; reference can deserialize null even with valid GUID).
            var behaviorParams = _agentObject.GetComponent<BehaviorParameters>();
            if (behaviorParams != null)
            {
                if (behaviorParams.Model == null)
                {
                    var model = Resources.Load<ModelAsset>(DefaultModelResourcePath);
                    if (model == null)
                    {
                        _skipReason =
                            $"Model missing at Resources/{DefaultModelResourcePath}.onnx — " +
                            "cannot run InferenceOnly. Train + copy ONNX into Resources/AiDriver/Policies/.";
                        Debug.LogWarning("[RealLoopInfer] " + _skipReason);
                        UnityEngine.Object.DestroyImmediate(_agentObject);
                        _agentObject = null;
                        return;
                    }
                    behaviorParams.Model = model;
                }
                behaviorParams.BehaviorType = BehaviorType.InferenceOnly;
            }

            _agent = _agentObject.GetComponent<AiDriverAgentBehaviour>();
            if (_agent == null)
            {
                Debug.LogError("[RealLoopInfer] spawned prefab is missing AiDriverAgentBehaviour.");
                UnityEngine.Object.DestroyImmediate(_agentObject);
                _agentObject = null;
                return;
            }
            _agent.Configure("default", spawnPos, heading);
            _agentObject.SetActive(true); // Agent.OnEnable + Initialize fire here, model is now assigned
            _agent.BindPolicy(_policyService);
            _agentReady = _agent.IsRegistered;

            // Visual cube tracks the simulated car position. Signal red so the
            // viewer can pick the agent out from terrain mesh in iso view.
            _carVisual = _factory.CreatePrimitive(PrimitiveType.Cube, "[RealLoopInfer] CarVisual",
                spawnPos + new Vector3(0f, 0.1f, 0f));
            _carVisual.transform.SetParent(SceneRoot.transform, false);
            _carVisual.transform.localScale = new Vector3(0.25f, 0.18f, 0.4f);
            var renderer = _carVisual.GetComponent<MeshRenderer>();
            if (renderer != null)
            {
                // Skip the terrain vertex-color shader for the car — the cube
                // primitive has no vertex colours so it'd render white. Pick the
                // first shader that actually honors _Color / _BaseColor.
                var mat = ResolveColoredMaterial("AiDriver_RealLoopInfer_CarMat",
                    new Color(0.92f, 0.18f, 0.18f, 1f)); // signal red
                renderer.sharedMaterial = mat;
            }
        }

        private void HookTickProxy()
        {
            var proxyGo = _factory.CreateEmpty("[RealLoopInfer] TickProxy");
            proxyGo.transform.SetParent(SceneRoot.transform, false);
            _tickProxy = proxyGo.AddComponent<RealisticLoopInferenceTickProxy>();
            _tickProxy.OnFixedUpdate = OnFixedUpdate;
            _tickProxy.OnUpdate = OnUpdate;
        }

        private void ApplyIsoCamera()
        {
            var cam = Camera.main;
            if (cam == null) return;
            float c = TrackPieceConstants.CellSize;
            var target = new Vector3(TerrainWidth * 0.5f * c, 0f, TerrainDepth * 0.5f * c);
            cam.transform.position = target + new Vector3(20f * c, 24f * c, -20f * c);
            cam.transform.LookAt(target);
        }

        private void AttachCarFollowCamera()
        {
            if (_carVisual == null) return;
            var cam = Camera.main;
            if (cam == null) return;
            // Reuse if the component is already on the camera (e.g. user toggled
            // and re-ran). OnDisable in CarFollowCamera restores the iso pose.
            var follow = cam.GetComponent<CarFollowCamera>();
            if (follow == null) follow = cam.gameObject.AddComponent<CarFollowCamera>();
            follow.Target = _carVisual.transform;
            follow.LocalOffset = new Vector3(0f, 1.4f, -2.6f);
            follow.LookAtOffset = new Vector3(0f, 0.3f, 0f);
            follow.PositionSmoothTime = 0.15f;
            follow.RotationLerpSpeed = 8f;
            follow.enabled = true;
        }

        private void OnFixedUpdate(float dt)
        {
            _carSim?.FixedTick(dt);
        }

        private void OnUpdate(float dt)
        {
            if (!_agentReady) return;
            if (!_carSim.TryGetState(_agent.CarId, out var state)) return;
            if (_carVisual == null) return;
            _carVisual.transform.position = state.Position + new Vector3(0f, 0.1f, 0f);
            _carVisual.transform.rotation = Quaternion.Euler(0f, state.Heading * Mathf.Rad2Deg, 0f);

            // Phase 2: mirror the policy's wall-feeler rays as Editor-only debug
            // lines (Scene view). Same constants the obs assembler uses, so what
            // you see is what the policy gets fed.
            if (_collision != null)
            {
                float h = state.Heading;
                float ch = Mathf.Cos(h);
                float sh = Mathf.Sin(h);
                Vector2 originXZ = new(state.Position.x, state.Position.z);
                Vector3 originW = state.Position + Vector3.up * 0.45f;
                float maxR = RacingObservationLayout.WallRayMaxMeters;
                for (int i = 0; i < RacingObservationLayout.WallRayCount; i++)
                {
                    float a = RacingObservationLayout.WallRayAnglesRad[i];
                    float ca = Mathf.Cos(a);
                    float sa = Mathf.Sin(a);
                    float dx = sh * ca + ch * sa;
                    float dz = ch * ca - sh * sa;
                    float d = _collision.RaycastWall(originXZ, new Vector2(dx, dz), maxR);
                    Vector3 endW = originW + new Vector3(dx, 0f, dz) * d;
                    float occ = 1f - Mathf.Clamp01(d / maxR);
                    Debug.DrawLine(originW, endW, Color.Lerp(Color.yellow, Color.red, occ));
                }
            }
        }

        // -------------------- helpers --------------------

        private Material ResolveOpaqueMaterial(string name)
        {
            var shader = Shader.Find("RaceConstructor/TerrainVertexColor")
                         ?? Shader.Find("Universal Render Pipeline/Lit")
                         ?? Shader.Find("Standard");
            return new Material(shader) { name = name };
        }

        // Cubes / primitives have no vertex colors, so the terrain vertex-color
        // shader renders them white. Use a shader that honors the Color/BaseColor
        // property and set both names so URP-Lit and Standard both apply.
        private Material ResolveColoredMaterial(string name, Color color)
        {
            var shader = Shader.Find("Universal Render Pipeline/Lit")
                         ?? Shader.Find("Standard")
                         ?? Shader.Find("Hidden/InternalErrorShader");
            var mat = new Material(shader) { name = name };
            mat.color = color;
            if (mat.HasProperty("_BaseColor")) mat.SetColor("_BaseColor", color);
            return mat;
        }

        private static void DestroyAny(UnityEngine.Object o)
        {
            if (o == null) return;
            if (Application.isPlaying) UnityEngine.Object.Destroy(o);
            else UnityEngine.Object.DestroyImmediate(o);
        }
    }

    /// <summary>
    /// Authored-only realistic-loop closure scenario. The shape catalog is fed
    /// exclusively by <see cref="AuthoredCardLoader"/> (the player's saved
    /// Track Editor cards) plus the four <see cref="ClosureTransitionShapes"/>
    /// — there is no <see cref="TrackShapeCatalogSeeder"/> recipe pool. The
    /// generator anchors with a 6–10 cell <c>closure:long-straight</c>, then
    /// composes authored cards for the body and closure-transition primitives
    /// (no walls / no kerbs) to bridge the closing seam. Validates that loop
    /// closure works without the previous runtime Bezier fallback.
    /// <para>Co-located with <see cref="RealisticLoopInferenceScenario"/> so
    /// Unity's auto-generated .csproj picks the type up without an editor
    /// refresh (per project memory <c>feedback_unity_new_cs_files</c>).</para>
    /// </summary>
    internal sealed class AuthoredOnlyClosureLoopScenario : DataDrivenScenario
    {
        private const int TerrainWidth = 60;
        private const int TerrainDepth = 60;
        private const int OriginX = 12;
        private const int OriginZ = 30;

        private ScenarioEventBus _eventBus;
        private ScenarioGameObjectFactory _factory;
        private TerrainService _terrain;
        private TerrainMeshBuilder _terrainMeshBuilder;
        private TrackPieceCatalog _pieceCatalog;
        private TrackPieceMeshBuilder _pieceMeshBuilder;
        private List<ITrackPlacementValidator> _validators;
        private TrackPlacementService _placement;
        private TrackCollisionService _collision;
        private TrackRibbonService _ribbonService;
        private ClosedLoopService _loopService;
        private TrackShapeCatalog _shapeCatalog;
        private ShapePreviewService _shapePreview;
        private ShapePlacementService _shapePlacement;
        private RealisticNativeCatalog _native;
        private RealisticTrackGenerator _generator;

        private GameObject _terrainObject;
        private Mesh _terrainMesh;
        private Material _terrainMaterial;

        private GenerationResult _lastResult;
        private int _authoredShapeCount;
        private int _closureShapeCount;
        private int _anchorStraightCells;

        public AuthoredOnlyClosureLoopScenario() : base(new TestScenarioDefinition(
            "ai-driver-authored-only-closure",
            "AI Driver — Authored-Only Closure (Live)",
            "Procedurally generates a closed loop using ONLY authored Track Editor " +
            "cards plus the four closure-transition primitives (rect-straight, 90° " +
            "curve, 45° curve right/left), each pinned to its bare (no-walls) " +
            "variant. The opening straight is a 6–10 cell long-anchor; the rest " +
            "of the body uses authored cards. No Bezier fallback. Pre-condition: " +
            "save at least one track via the Track Editor (F2) before running. " +
            "Same seed → same loop.",
            new[]
            {
                new ScenarioParameter("seed", "Seed", typeof(int), 1234, 0, 1_000_000),
                new ScenarioParameter("targetLengthCells", "Target Length (cells)", typeof(int), 60, 20, 200),
                new ScenarioParameter("turnDensity", "Turn Density (0..1)", typeof(float), 0.4f, 0f, 1f),
                new ScenarioParameter("beamWidth", "Beam Width", typeof(int), 16, 1, 64),
                new ScenarioParameter("attempts", "Attempts (RecipeAttempts)", typeof(int), 12, 1, 32),
                new ScenarioParameter("anchorStraightCells", "Anchor Straight (cells, 6-10)", typeof(int), 8,
                    ClosureTransitionShapes.LongStraightMinCells,
                    ClosureTransitionShapes.LongStraightMaxCells),
            }))
        { }

        protected override void ExecuteInternal(ScenarioParameterOverrides overrides)
        {
            int seed = ResolveParam<int>(overrides, "seed");
            int targetLengthCells = ResolveParam<int>(overrides, "targetLengthCells");
            float turnDensity = ResolveParam<float>(overrides, "turnDensity");
            int beamWidth = ResolveParam<int>(overrides, "beamWidth");
            int attempts = ResolveParam<int>(overrides, "attempts");
            _anchorStraightCells = ResolveParam<int>(overrides, "anchorStraightCells");

            var stage = CurriculumStages.Default;

            _eventBus = new ScenarioEventBus();
            _factory = new ScenarioGameObjectFactory();

            BuildTerrain();
            BuildTrackServices();
            BuildShapeServicesAuthoredOnly();
            BuildLoopServices();
            GenerateLoop(seed, stage, targetLengthCells, turnDensity, beamWidth, attempts);
            BuildRibbon();
            ApplyIsoCamera();

            Debug.Log($"[AuthoredClosure] stage={stage.Name} seed={seed} authored={_authoredShapeCount} " +
                      $"closure={_closureShapeCount} anchor={_anchorStraightCells} " +
                      $"loopClosed={_loopService?.IsLoopClosed} success={_lastResult.Success} " +
                      $"placed={_lastResult.PlacedPieces} length={_lastResult.TotalLength:F1}m " +
                      $"reason='{_lastResult.FailureReason}'");
        }

        protected override ScenarioVerificationResult VerifyInternal(ScenarioParameterOverrides overrides)
        {
            // Clean clone has no authored tracks on disk. Without them this scenario
            // cannot exercise its core invariant — skip rather than fail so CI stays
            // green. Local users hit the precondition message after they save a
            // track via the Track Editor (F2).
            if (_authoredShapeCount == 0)
            {
                return ScenarioVerificationResult.Skip(
                    "AuthoredCardLoader registered 0 shapes — save at least one track " +
                    "via the Track Editor (F2) before running this scenario.");
            }

            var checks = new List<ScenarioVerificationResult.CheckResult>
            {
                new("authored shapes present",
                    _authoredShapeCount > 0,
                    "AuthoredCardLoader registered 0 shapes — save at least one track via the Track Editor (F2) before running this scenario."),
                new("closure shapes registered",
                    _closureShapeCount >= 4,
                    $"ClosureTransitionShapes registered {_closureShapeCount} shapes; expected ≥4."),
                new("anchor in [6,10] range",
                    _anchorStraightCells >= ClosureTransitionShapes.LongStraightMinCells
