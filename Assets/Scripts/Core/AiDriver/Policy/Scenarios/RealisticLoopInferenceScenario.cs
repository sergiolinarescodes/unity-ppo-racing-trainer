    /// Realistic F1-style loop generated by <see cref="RealisticTrackGenerator"/>,
    /// then driven by the trained <see cref="AiDriverAgentBehaviour"/> in
    /// <c>InferenceOnly</c> mode. Uses the AiDriverAgent_Test.prefab in
