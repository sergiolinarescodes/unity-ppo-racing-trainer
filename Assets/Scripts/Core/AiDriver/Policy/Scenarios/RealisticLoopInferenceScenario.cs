            {
                Debug.LogWarning($"[AuthoredClosureLib] backfill skipped — no {outDir} dir.");
                return 0;
            }
            var stage = CurriculumStages.Default;

            var files = Directory.GetFiles(outDir, "*.json");
            int updated = 0;
            int unchanged = 0;
            int failed = 0;
            bool cancelled = false;
            try
